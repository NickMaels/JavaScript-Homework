let min=prompt('Введите Число от 0 до 59:');

if(min<0 || min>59) alert('Данное число не находится в интервале от 0 до 59, попробуйте снова.');
else
{
    if(min>=0 && min<=14)alert('Первая четверть');
    else
    {
        if(min>=15 && min<=30)alert('Вторая четверть');
        else
        {
            if(min>=31 && min<=45)alert('Третья четверть');
            else
            {
                if(min>=46 && min<=59)alert('Четвертая четверть');
            }
        }
    }
}